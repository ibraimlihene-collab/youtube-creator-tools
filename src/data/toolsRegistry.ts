export type ToolCategory =
  | 'editing'
  | 'thumbnails'
  | 'writing'
  | 'seo'
  | 'growth'
  | 'planning'
  | 'engagement'
  | 'business'
  | 'local';

export type ToolKind = 'local' | 'ai' | 'hybrid';

export type FieldType = 'text' | 'textarea' | 'select' | 'number';

export interface ToolField {
  name: string;
  type: FieldType;
  labelEn: string;
  labelAr: string;
  placeholderEn?: string;
  placeholderAr?: string;
  required?: boolean;
  options?: { value: string; labelEn: string; labelAr: string }[];
  rows?: number;
}

export interface ToolDef {
  id: string;
  icon: ElementType;
  path: string;
  category: ToolCategory;
  kind: ToolKind;
  badge?: 'ai' | 'local' | 'new';
  /** Custom React feature component path key — if set, use dedicated UI */
  custom?:
    | 'silenceRemover'
    | 'cpmCalculator'
    | 'thumbnailDownloader'
    | 'thumbnailPreviewer'
    | 'colorPaletteGenerator'
    | 'hashtagGenerator';
  titleEn: string;
  titleAr: string;
  descEn: string;
  descAr: string;
  fields?: ToolField[];
  /** Maps to server prompt toolId (defaults to id) */
  aiToolId?: string;
}

